package aula6e7;

public class ConversaoDeUnidadesDeTempo {

    public static int minutoParaSegundos (int minutos) {
        return minutos * 60;
    }

    public static int horaParaMinutos (int horas) {
        return horas * 60;
    }

    public static int diaParaHoras (int dias) {
        return dias * 24;
    }
}
